/// Case-fold one UTF-16 unit (Apple `gLowerCaseTable`). Returns 0 for an
/// "ignored" code point (the caller drops it); NUL folds to 0xFFFF.
pub fn case_fold(c: u16) -> u16 {
    let hi = CASE_FOLD[(c >> 8) as usize];
    if hi != 0 {
        CASE_FOLD[(hi + (c & 0xff)) as usize]
    } else {
        c
    }
}

const HANGUL_S_BASE: u32 = 0xac00;
const HANGUL_L_BASE: u16 = 0x1100;
const HANGUL_V_BASE: u16 = 0x1161;
const HANGUL_T_BASE: u16 = 0x11a7;
const HANGUL_S_COUNT: u32 = 11172;
const HANGUL_T_COUNT: u32 = 28;
const HANGUL_N_COUNT: u32 = 21 * 28;

/// Algorithmic Hangul syllable decomposition (Unicode/TN1150). Writes 2-3 jamo
/// into `out` and returns the count, or 0 when `uc` is not a Hangul syllable.
fn decompose_hangul(uc: u16, out: &mut [u16; 3]) -> usize {
    let index = uc as u32;
    if !(HANGUL_S_BASE..HANGUL_S_BASE + HANGUL_S_COUNT).contains(&index) {
        return 0;
    }
    let index = index - HANGUL_S_BASE;
    out[0] = HANGUL_L_BASE + (index / HANGUL_N_COUNT) as u16;
    out[1] = HANGUL_V_BASE + ((index % HANGUL_N_COUNT) / HANGUL_T_COUNT) as u16;
    let t = HANGUL_T_BASE + (index % HANGUL_T_COUNT) as u16;
    if t != HANGUL_T_BASE {
        out[2] = t;
        3
    } else {
        2
    }
}

/// Table decomposition for a non-Hangul unit. Returns the static decomposed
/// sequence, or `None` if the unit has no canonical decomposition.
fn decompose_nonhangul(uc: u16) -> Option<&'static [u16]> {
    let mut off = DECOMPOSE[((uc >> 12) & 0xf) as usize];
    if off == 0 || off == 0xffff {
        return None;
    }
    off = DECOMPOSE[(off + ((uc >> 8) & 0xf)) as usize];
    if off == 0 {
        return None;
    }
    off = DECOMPOSE[(off + ((uc >> 4) & 0xf)) as usize];
    if off == 0 {
        return None;
    }
    off = DECOMPOSE[(off + (uc & 0xf)) as usize];
    let size = (off & 3) as usize;
    if size == 0 {
        return None;
    }
    let start = (off / 4) as usize;
    Some(&DECOMPOSE[start..start + size])
}

/// Canonically decompose one UTF-16 unit into `buf`, returning the decomposed
/// slice (1-3 units). Units with no decomposition return themselves.
pub fn decompose(uc: u16, buf: &mut [u16; 3]) -> &[u16] {
    let n = decompose_hangul(uc, buf);
    if n != 0 {
        return &buf[..n];
    }
    if let Some(seq) = decompose_nonhangul(uc) {
        buf[..seq.len()].copy_from_slice(seq);
        return &buf[..seq.len()];
    }
    buf[0] = uc;
    &buf[..1]
}

/// Encode a name as the canonical HFS+ on-disk form: UTF-16 units, each
/// canonically decomposed per Apple's TN1150 table. This is how classic Mac OS
/// stores HFS+ names; using it (rather than Rust's NFD) keeps the bytes
/// byte-identical to what the OS would write, so lookups by either side match.
pub fn decompose_str(name: &str) -> Vec<u16> {
    let mut out = Vec::with_capacity(name.len());
    let mut buf = [0u16; 3];
    let mut unit_buf = [0u16; 2];
    for ch in name.chars() {
        for &unit in ch.encode_utf16(&mut unit_buf).iter() {
            out.extend_from_slice(decompose(unit, &mut buf));
        }
    }
    out
}
