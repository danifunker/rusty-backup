//! Geometry-independent NTFS data tables for the clean-room formatter.
//!
//! Per `docs/ntfs_format_cleanroom_notes.md`, every byte array here is a
//! *data table* whose content does not depend on the volume's sector or
//! cluster size: the standard `$AttrDef` table, the Unicode `$UpCase`
//! mapping, the standard `$Secure:$SDS` security-descriptor stream and the
//! small resident attribute payloads ($SECURITY_DESCRIPTOR, the `$Secure`
//! $SDH/$SII index roots, `$UpCase:$Info`, the `$Extend` $I30 index root).
//! They are observed from `mkntfs` output (a published, royalty-free format)
//! and reproduced verbatim; the geometry-dependent backbone (boot/BPB, FILE
//! record headers, runlists, fixups, bitmaps, the root index) is synthesised
//! parametrically in `ntfs_format.rs` and is NOT taken from any blob.

/// Standard `$AttrDef` table (the fixed Microsoft attribute-definition list).
pub(super) static ATTRDEF: &[u8] = include_bytes!("ntfs_template/attrdef.bin"); // 2560
/// Unicode uppercase mapping table (`$UpCase` $DATA), 65536 UTF-16 code units.
pub(super) static UPCASE: &[u8] = include_bytes!("ntfs_template/upcase.bin"); // 131072
/// Standard `$Secure:$SDS` security-descriptor stream.
pub(super) static SECURE_SDS: &[u8] = include_bytes!("ntfs_template/secure_sds.bin"); // 262396
/// Root directory non-resident `$SECURITY_DESCRIPTOR` value.
pub(super) static ROOT_SECDESC: &[u8] = include_bytes!("ntfs_template/root_secdesc.bin"); // 4140

